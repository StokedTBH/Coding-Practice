// Some Math Object Work

console.log(Math.PI);
console.log(Math.E);

let x = 3.21;
let y = 2;
let z;

z = Math.round(x);
console.log(z);
z = Math.floor(x);
console.log(z);
z = Math.ceil(x);
console.log(z);
z = Math.trunc(x);
console.log(z);
z = Math.pow(x, y);
console.log(z);
z = Math.sqrt(x);
console.log(z);
z = Math.log(x);
console.log(z);
z = Math.abs(x);
console.log(z);

// Gives the sign (-1 if negative & 1 if positive)
z = Math.sign(x)
console.log(z);

// Gets maximum and minimum values of selected numbers
z = Math.max(x, y, z);
console.log(z);
z = Math.min(x, y, z);
console.log(z);

// Takes as radians
z = Math.sin(x)
console.log(z);
z = Math.cos(x)
console.log(z);
z = Math.tan(x)
console.log(z);


